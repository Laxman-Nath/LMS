    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
//        setVisible(false);
        String bookID=jTextField2.getText();
        String name=jTextField3.getText();
        String publisher=jTextField4.getText();
        String price=jTextField5.getText();
        String publishedYear=jTextField6.getText();
        try{
            Connection conn=getConn();
            PreparedStatement ps=conn.prepareStatement("Insert into book values(?,?,?,?,?)");
            ps.setString(1, bookID);
            ps.setString(2, name);
            ps.setString(3, publisher);
            ps.setString(4, price);
            ps.setString(5, publishedYear);
            int affectedRows=ps.executeUpdate();
            if(affectedRows>0){
                 JOptionPane.showMessageDialog(null, "Saved book successfully");
                 setVisible(false);
                 new NewBook().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Error saving book");
                setVisible(false);
                new NewBook().setVisible(true);
            }
        }
        catch(Exception e){
//            JOptionPane.showMessageDialog(null,"BookId already exists!");
//            setVisible(false);
//            new NewBook().setVisible(true);
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

